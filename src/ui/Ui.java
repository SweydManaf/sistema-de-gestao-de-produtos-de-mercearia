    public void updateTable() throws Exception {
        List<Product> products = controller.getAll();
        List<Object[]> data = products.stream().map(
                p -> new Object[]{
                        p.getId(), p.getName(), p.getPrice(), p.getCategory(), p.getExpiration_date(), p.getCreated_at()
                }
        ).collect(Collectors.toList());
        Object[][] dataArray = new Object[data.size()][];
        for (int i = 0; i < data.size(); i++){
            dataArray[i] = data.get(i);
        }

        table.setModel(new javax.swing.table.DefaultTableModel(
                dataArray,
                new String [] {
                        "ID", "Nome", "Pre√ßo", "Categoria", "Prazo de Validade", "Data de Registro"
                }
        ){
            public boolean isCellEditable(int row, int column){
                return false;
            }
        });
    }
